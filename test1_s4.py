# Ajout de clés et de valeurs au dictionnaire "dico" :
dico = {}
dico["arbre"] = "vert"  # [clé] = valeur
dico["plâtre"] = "blanc"
dico["pneu"] = 5
# Pour accéder à un élément dans un dictionnaire :
print(dico["pneu"])

# Pour imprimer tout le contenu du dictionnaire "dico" :
print(dico)
# On peut aussi écrire :
a={'plâtre': 'blanc', 'pneu': 5, 4: 'vert'}
print(a['plâtre'])
print(a[4])



